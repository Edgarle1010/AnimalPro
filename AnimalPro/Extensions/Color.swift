    let accent = Color("AccentColor")
    let primary = Color("PrimaryColor")
    let secondary = Color("SecondaryColor")
    let tertiary = Color("TertiaryColor")
    let red = Color("RedColor")
    let facebookBlue = Color("FacebookBlueColor")
}


